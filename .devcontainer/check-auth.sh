#!/usr/bin/env bash
# Runs on every container start.
#
# Loads secrets from .devcontainer/.env into this script's env, and installs
# a /etc/profile.d hook so every new login shell also sources them. This
# decouples env delivery from `runArgs --env-file` (which only applies at
# container creation), so Keychain updates take effect on restart rather
# than requiring a rebuild.

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
ENV_FILE="$SCRIPT_DIR/.env"
PROFILE_HOOK="/etc/profile.d/devcontainer-env.sh"

if [ -f "$ENV_FILE" ]; then
  set -a
  # shellcheck disable=SC1090
  . "$ENV_FILE"
  set +a

  TMP=$(mktemp)
  cat > "$TMP" <<EOF
#!/bin/sh
# Auto-generated by .devcontainer/check-auth.sh — do not edit.
if [ -f "$ENV_FILE" ]; then
  set -a
  . "$ENV_FILE"
  set +a
fi
EOF
  sudo install -m 0644 -o root -g root "$TMP" "$PROFILE_HOOK"
  rm -f "$TMP"
fi

# Restore ~/.claude.json symlink if Claude Code replaced it with a regular file
CLAUDE_JSON_REAL=/home/node/.claude/claude-code/claude.json
CLAUDE_JSON=/home/node/.claude.json
if [ ! -L "$CLAUDE_JSON" ] && [ -f "$CLAUDE_JSON_REAL" ]; then
  [ -f "$CLAUDE_JSON" ] && mv "$CLAUDE_JSON" "${CLAUDE_JSON}.orig"
  ln -s "$CLAUDE_JSON_REAL" "$CLAUDE_JSON"
fi

echo ""
echo "══════════════════════════════════════════════════════"
echo "  Checking authentication status..."
echo "══════════════════════════════════════════════════════"

# GitHub CLI — gh reads GITHUB_TOKEN from the environment automatically.
if gh auth status &>/dev/null; then
  echo "  ✓ GitHub CLI — authenticated"
elif [ -n "${GITHUB_TOKEN:-}" ]; then
  echo "  ✗ GitHub CLI — GITHUB_TOKEN is set but gh rejected it (expired or wrong scopes?)"
else
  echo "  ✗ GitHub CLI — not authenticated"
  echo "    Store a PAT:  security add-generic-password -a \"\$USER\" -s \"GITHUB_TOKEN\" -w \"ghp_...\""
  echo "    Or run:       gh auth login"
fi

# Claude Code — OAuth token preferred, API key fallback, then persisted OAuth in ~/.claude
if [ -n "${CLAUDE_CODE_OAUTH_TOKEN:-}" ]; then
  echo "  ✓ Claude Code — OAuth token present (subscription billing)"
elif [ -n "${ANTHROPIC_API_KEY:-}" ]; then
  echo "  ✓ Claude Code — API key present (pay-per-token fallback)"
elif claude auth status &>/dev/null; then
  echo "  ✓ Claude Code — authenticated (persisted OAuth in ~/.claude volume)"
else
  echo "  ✗ Claude Code — not authenticated"
  echo "    Preferred: run 'claude setup-token' on your Mac, then:"
  echo "      security add-generic-password -a \"\$USER\" -s \"CLAUDE_OAUTH_TOKEN\" -w \"sk-ant-oat01-...\""
fi

if command -v claude &>/dev/null; then
  echo "  ✓ Claude CLI — $(claude --version 2>/dev/null | head -1)"
else
  echo "  ✗ Claude CLI — not installed (run: bash .devcontainer/setup.sh)"
fi

# Git identity
if git config user.email &>/dev/null; then
  echo "  ✓ Git identity — $(git config user.name) <$(git config user.email)>"
else
  echo "  ✗ Git identity — not configured"
  echo "    Run:  git config --global user.name \"Your Name\""
  echo "          git config --global user.email \"you@example.com\""
fi

# OpenTofu (optional — only report if installed)
if command -v tofu &>/dev/null; then
  TOFU_VERSION=$(tofu version 2>/dev/null | head -1 || echo "unknown")
  echo "  ✓ OpenTofu — $TOFU_VERSION"
fi

echo "══════════════════════════════════════════════════════"
echo ""
